x=int(input("enter your age:"))
match x:
    case x if x<=10:
        print("child")
    case x if 10<x<=20:
        print("teen")
    case x if 20<x<=40:
        print("young")
    case x if 40<x<=60:
        print("experienced")
    case x if x>=60:
        print("senior citizen")
print()        
      
      
          
      
      
          
      
      
      
      
      
           
      
      
      
 
